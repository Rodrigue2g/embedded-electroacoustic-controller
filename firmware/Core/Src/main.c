static inline void itm_putc(char c) { // Stimulus port 0
  if ((ITM->TCR & ITM_TCR_ITMENA_Msk) && (ITM->TER & 1)) {
    while (ITM->PORT[0].u32 == 0);
    ITM->PORT[0].u8 = (uint8_t)c;
  }
}
static inline void itm_u16(uint16_t v){
  itm_putc(v & 0xFF);
  itm_putc((v >> 8) & 0xFF);
}

